package com.ecs160.nittacraft;

public class SRectangle {
    public int DXPosition;
    public int DYPosition;
    public int DWidth;
    public int DHeight;

    public SRectangle() {}

    public SRectangle(int xPos, int yPos, int width, int height) {
        DXPosition = xPos;
        DYPosition = yPos;
        DWidth = width;
        DHeight = height;
    }
}